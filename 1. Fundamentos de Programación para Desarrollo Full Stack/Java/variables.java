// Declara tres variables de diferentes tipos primitivos y muestra sus valores en consola.

public class Variables {
    public static void main(String[] args) {
        int entero = 10;
        double decimal = 10.5;
        char caracter = 'a';

        System.out.println("El valor de la variable entero es: " + entero);
        System.out.println("El valor de la variable decimal es: " + decimal);
        System.out.println("El valor de la variable caracter es: " + caracter);
    }
}